#!/usr/bin/python3
def add(a, b):
    """My addition function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a + b
    """
    return (a + b)

def mul(a, b):
    "function to multiply"
    return (a * b)

def div(a, b):
    "function to divide"
    return (a / b)
